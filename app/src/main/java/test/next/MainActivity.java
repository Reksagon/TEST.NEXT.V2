    @Override
    public boolean onSupportNavigateUp() {
        NavController navController = Navigation.findNavController(this, R.id.nav_host_fragment_content_main);
        return NavigationUI.navigateUp(navController, mAppBarConfiguration)
                || super.onSupportNavigateUp();
    }

    @Override
    public void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);

        // Result returned from launching the Intent from GoogleSignInClient.getSignInIntent(...);
        if (requestCode == 123) {
            // The Task returned from this call is always completed, no need to attach
            // a listener.
            Task<GoogleSignInAccount> task = GoogleSignIn.getSignedInAccountFromIntent(data);
            handleSignInResult(task);
        }
        else
        {
            System.exit(0);
        }
    }

    private void UpdateUI(GoogleSignInAccount account) throws IOException {
        TextView name = findViewById(R.id.name_account);
        name.setText(account.getGivenName());
        TextView email = findViewById(R.id.email_account);
        email.setText(account.getEmail());
        ImageView imageView = findViewById(R.id.photo_account);
        Glide.with(getApplicationContext()).load(account.getPhotoUrl().toString())
                .thumbnail(0.5f)
                .diskCacheStrategy(DiskCacheStrategy.ALL)
                .into(imageView);
    }


    private void handleSignInResult(Task<GoogleSignInAccount> completedTask) {
        try {
            GoogleSignInAccount account = completedTask.getResult(ApiException.class);
            Toast.makeText(getApplicationContext(), "Success", Toast.LENGTH_SHORT).show();
            if(account != null)
                UpdateUI(account);

        } catch (ApiException | FileNotFoundException e) {
            // The ApiException status code indicates the detailed failure reason.
            // Please refer to the GoogleSignInStatusCodes class reference for more information.
            //Log.w(TAG, "signInResult:failed code=" + e.getStatusCode());
            // updateUI(null);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

